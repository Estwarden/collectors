def jitter(max_seconds=120):
    """Sleep 0..max_seconds to break timing fingerprint. Call at collector start."""
    time.sleep(random.uniform(0, max_seconds))


def jitter_sleep(base, spread=0.5):
    """Sleep base +/- spread*base seconds. Use between requests instead of fixed sleep."""
    time.sleep(base * random.uniform(1 - spread, 1 + spread))
